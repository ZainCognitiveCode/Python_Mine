# Some useful builtin function
# print
# input
# type
a = 3
type(a)
print(type(a))

# int etc.
int(5.6)
print(int(5.6))

# abs
abs(-4)
print(abs(-4))

# power
pow(2,-3)
print(pow(2,-3))

# min/max
min([2,1,3,0])
print(min([2,1,3,0]))
# or you can use max

# round
c = 22/7
round(c)
print(round(c))

# divmod

divmod(5,2)
print(divmod(5,2))

# bin/oct/hex

bin(4)
print(bin(4))

# id

a = 3
id(a)
print(id(a))

# ord
ord('c')
print(ord('c'))

# len

len([1,2,3,4,5])
print(len([1,2,3,4,5]))

# sum
sum([1,2,3,4,5])
print(sum([1,2,3,4,5]))

# help
help('print')
print(help('print'))
